def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
     return n1/n2

operations = {
        "+": add, "-": subtract, "*": multiply, "/": divide,
    }

while True:
    first_num = float(input("What's the first number?"))
    while True:
        operation = input("Pick an operation: ")
        next_num = float(input("What's the next number?"))

        math = operations[operation](first_num, next_num)
        print(f"{first_num} {operation} {next_num} = {math}")

        more = input(f"Type 'y' to continue calculating with {math}, or type 'n' to start a new calculation: ").lower()
        if more == 'y':
            first_num = math
        else:
            break


